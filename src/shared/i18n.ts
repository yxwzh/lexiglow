export function t(
  languageCode: SupportedLearnerLanguageCode | string | undefined,
  key: UiStringKey,
  variables?: Record<string, string | number>,
): string {
  const dictionary = languageCode && languageCode in UI_STRINGS
    ? UI_STRINGS[languageCode as SupportedLearnerLanguageCode]
    : undefined;
  const template = dictionary?.[key] ?? UI_STRINGS_EN[key];

  if (!variables) {
    return template;
  }

  return template.replace(/\{(\w+)\}/g, (_match, name: string) => {
    return Object.prototype.hasOwnProperty.call(variables, name)
      ? String(variables[name])
      : `{${name}}`;
  });
}
